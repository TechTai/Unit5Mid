package nyc.c4q.randomuserapp.Model;

public class Info {
    private String version;
    private int page;
    private int results;
    private String seed;

    public String getVersion() {
        return version;
    }

    public int getPage() {
        return page;
    }

    public int getResults() {
        return results;
    }

    public String getSeed() {
        return seed;
    }
}
